import { ref, onMounted, onBeforeUnmount } from 'vue'
import gsap from 'gsap'

const root = ref(null)
const fillReveal = useSvgColourReveal()

let ctx
let tl
let clickTl
let clickDelay

function play() {
  tl?.play()

  clickDelay?.kill()
  clickDelay = gsap.delayedCall(0.4, () => {
    clickTl?.restart()
  })
}

function reverse() {
  clickDelay?.kill()
  clickDelay = null

  clickTl?.pause(0)

  gsap.set('.skills-cursor', { scale: 1 })

  tl?.reverse()
}

function restart() {
  tl?.restart()

  clickDelay?.kill()
  clickDelay = gsap.delayedCall(0.4, () => {
    clickTl?.restart()
  })
}

